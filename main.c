#include <stdio.h>

int main() {
    int d, x;

    // Отримуємо вхідне число
    printf("Введіть число d: ");
    scanf("%d", &d);

    // Обчислюємо x (правильний порядок цифр)
    x = (d % 10) * 100 + (d / 100) + ((d / 10) % 10) * 10;

    // Виводимо результат
    printf("Число x: %d\n", x);

    return 0;
}
