#include <stdio.h>

int main() {
    int d, x; // Оголошуємо змінні для введеного числа d і результату x

    // Введення числа 
    printf("Введіть число d: ");
    scanf("%d", &d);

    // Розбиття числа на окремі цифри
    int a = d / 100;       // Перша цифра (сотні)
    int b = (d / 10) % 10; // Друга цифра (десятки)
    int c = d % 10;        // Третя цифра (одиниці)

    // Формування нового числа: остання цифра стає першою, а перші дві зміщуються праворуч
    x = c * 100 + b * 10 + a;

    // Виведення результату
    printf("Число x: %d\n", x);

    return 0;
}